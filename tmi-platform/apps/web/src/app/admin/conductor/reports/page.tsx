"use client";

import { useEffect, useState } from "react";

type Report = {
  id: string;
  reportType: string;
  subsystem: string;
  status: string;
  score: number;
  issues: string[];
  recommendations: string[];
  generatedBy: string;
  createdAt: string;
};

const STATUS_COLOR: Record<string, string> = {
  healthy: "text-green-400 border-green-500/30 bg-green-950/20",
  degraded: "text-yellow-400 border-yellow-500/30 bg-yellow-950/20",
  critical: "text-red-400 border-red-500/30 bg-red-950/20",
};

const SUBSYSTEMS = ["all", "magazine", "economy", "social", "auth", "games", "live", "build", "security"];

export default function ConductorReportsPage() {
  const [reports, setReports] = useState<Report[]>([]);
  const [total, setTotal] = useState(0);
  const [loading, setLoading] = useState(true);
  const [selected, setSelected] = useState("all");
  const [generating, setGenerating] = useState(false);

  const load = (sub?: string) => {
    setLoading(true);
    const params = new URLSearchParams({ limit: "30" });
    if (sub && sub !== "all") params.set("subsystem", sub);
    fetch(`/api/conductor/reports?${params}`)
      .then((r) => r.json())
      .then((d) => { setReports(d.reports ?? []); setTotal(d.total ?? 0); setLoading(false); })
      .catch(() => setLoading(false));
  };

  useEffect(() => { load(); }, []);

  const handleGenerate = async (sub: string) => {
    setGenerating(true);
    await fetch("/api/conductor/reports/generate", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ subsystem: sub === "all" ? "platform" : sub }),
    });
    load(sub === "all" ? undefined : sub);
    setGenerating(false);
  };

  return (
    <main className="min-h-screen bg-black text-white p-6">
      <div className="max-w-5xl mx-auto">
        <div className="flex items-center justify-between mb-6">
          <div>
            <a href="/admin/conductor" className="text-white/30 text-xs hover:text-white/60 mb-1 block">← MC Command Center</a>
            <h1 className="text-2xl font-black">Health Reports</h1>
            <p className="text-white/40 text-sm">{total} reports · Generated by Michael Charlie</p>
          </div>
          <button
            onClick={() => handleGenerate(selected)}
            disabled={generating}
            className="text-xs px-4 py-2 rounded-lg bg-blue-600 hover:bg-blue-500 text-white font-semibold disabled:opacity-50 transition-colors"
          >
            {generating ? "Generating..." : `Generate Report`}
          </button>
        </div>

        {/* Subsystem filter */}
        <div className="flex gap-2 flex-wrap mb-6">
          {SUBSYSTEMS.map((s) => (
            <button
              key={s}
              onClick={() => { setSelected(s); load(s === "all" ? undefined : s); }}
              className={`text-xs px-3 py-1.5 rounded-full border transition-colors ${selected === s ? "bg-white/20 border-white/40 text-white" : "border-white/10 text-white/40 hover:text-white/70"}`}
            >
              {s}
            </button>
          ))}
        </div>

        {loading ? (
          <div className="text-white/40 text-sm">Loading reports...</div>
        ) : reports.length === 0 ? (
          <div className="rounded-lg border border-white/10 bg-white/5 p-8 text-center text-white/30 text-sm">
            No health reports yet. Click &ldquo;Generate Report&rdquo; to create the first one.
          </div>
        ) : (
          <div className="space-y-3">
            {reports.map((r) => (
              <div key={r.id} className={`rounded-lg border p-4 ${STATUS_COLOR[r.status] ?? "border-white/10 bg-white/5"}`}>
                <div className="flex items-start justify-between gap-4">
                  <div>
                    <div className="flex items-center gap-2 mb-1">
                      <span className="text-xs font-mono uppercase">{r.subsystem}</span>
                      <span className="text-xs text-white/40">{r.reportType}</span>
                      <span className="text-xs font-bold">Score: {r.score.toFixed(1)}</span>
                    </div>
                    {r.issues.length > 0 && (
                      <div className="text-xs text-white/60 mt-1">
                        Issues: {r.issues.join(", ")}
                      </div>
                    )}
                    {r.recommendations.length > 0 && (
                      <div className="text-xs text-white/40 mt-1">
                        Recommendations: {r.recommendations.join(", ")}
                      </div>
                    )}
                  </div>
                  <div className="text-xs text-white/30 whitespace-nowrap">{new Date(r.createdAt).toLocaleString()}</div>
                </div>
              </div>
            ))}
          </div>
        )}
      </div>
    </main>
  );
}
