"use client";
import { useEffect, useState } from "react";
import { useParams, useRouter } from "next/navigation";
import { motion } from "framer-motion";
import Link from "next/link";

interface MagazineJob {
  jobId: string;
  templateId: string;
  variables: Record<string, string>;
  status: string;
  slug: string;
  createdAt: string;
}

// Bento card shapes indexed by position in a layout of N cards
const CLIP_PATHS = [
  "polygon(0 0, 100% 0, 100% 85%, 92% 100%, 0 100%)",          // card 0: bottom-right notch
  "polygon(0 0, 95% 0, 100% 8%, 100% 100%, 5% 100%, 0 92%)",   // card 1: top-right + bottom-left notch
  "polygon(8% 0, 100% 0, 100% 100%, 0 100%, 0 10%)",            // card 2: top-left diagonal
  "polygon(0 0, 92% 0, 100% 12%, 100% 100%, 0 100%)",           // card 3: top-right ramp
  "polygon(0 8%, 100% 0, 100% 100%, 0 92%)",                    // card 4: parallelogram
  "polygon(5% 0, 100% 0, 95% 100%, 0 100%)",                    // card 5: leaning right
];

const ACCENT_COLORS = ["#00FFFF", "#FF2DAA", "#AA2DFF", "#FFD700", "#00FF88", "#FF9500"];

interface RenderedSection {
  title: string;
  body: string;
  icon: string;
  accentIndex: number;
}

function buildSections(templateId: string, vars: Record<string, string>): RenderedSection[] {
  const fill = (tpl: string) =>
    tpl.replace(/\{(\w+)\}/g, (_, k) => vars[k] ?? `[${k}]`);

  switch (templateId) {
    case "artist-spotlight":
      return [
        { title: fill("WHO IS {artistName}?"), body: fill("An in-depth look at {artistName} — from the underground to the spotlight."), icon: "🎤", accentIndex: 0 },
        { title: "DISCOGRAPHY", body: fill("Catalog deep-dive: every drop, every era, every collab in {artistName}'s timeline."), icon: "💿", accentIndex: 1 },
        { title: "UPCOMING EVENTS", body: fill("Where to catch {artistName} live — dates, venues, and pre-sale access."), icon: "📅", accentIndex: 3 },
        { title: "FAN MESSAGE", body: fill("The community speaks: what fans have to say about {artistName}'s impact."), icon: "❤️", accentIndex: 4 },
      ];
    case "beat-review":
      return [
        { title: fill("TRACK: {trackTitle}"), body: fill("First listen breakdown — production, drums, melody, and vibe of {trackTitle}."), icon: "🎹", accentIndex: 1 },
        { title: "PRODUCTION NOTES", body: "Key samples, 808 tuning, mix decisions, and the story behind the session.", icon: "🎛️", accentIndex: 2 },
        { title: "RATING", body: "TMI Rating: ★★★★☆ — hard-hitting production with replay value.", icon: "⭐", accentIndex: 3 },
        { title: "WHERE TO BUY", body: fill("License {trackTitle} exclusively on TMI Beat Store. Lease or exclusive."), icon: "🛒", accentIndex: 0 },
      ];
    case "scene-report":
      return [
        { title: fill("HEADLINE: {city} UNDERGROUND"), body: fill("The {city} scene is moving. Here's what's happening right now."), icon: "🌆", accentIndex: 2 },
        { title: "FEATURED ARTISTS", body: fill("Top names rising from {city}: the emcees, producers, and crews to watch."), icon: "🎯", accentIndex: 0 },
        { title: "VENUE SPOTLIGHT", body: fill("The rooms and spaces fueling the {city} movement — from basement to main stage."), icon: "🏟️", accentIndex: 4 },
        { title: "UPCOMING", body: fill("Next events in {city}: shows, cyphers, showcases, and open mics on deck."), icon: "📍", accentIndex: 1 },
      ];
    case "cypher-recap":
      return [
        { title: fill("WINNER — {date}"), body: fill("The champion crowned at the {date} cypher. Bar-for-bar breakdown."), icon: "🏆", accentIndex: 3 },
        { title: "HIGHLIGHTS", body: fill("Top moments from {date}: the verses, the crowd reactions, the turning points."), icon: "🔥", accentIndex: 0 },
        { title: "STATS", body: fill("Votes cast, rounds battled, and audience engagement from the {date} session."), icon: "📊", accentIndex: 2 },
        { title: "NEXT CYPHER", body: "Register for the next edition — slots open now. Tier upgrades unlock bonus time.", icon: "⚡", accentIndex: 1 },
      ];
    default:
      return [
        { title: "AUTO-GENERATED CONTENT", body: "This article was generated by the TMI Magazine Bot.", icon: "🤖", accentIndex: 0 },
      ];
  }
}

export default function MagazineAutoPage() {
  const params = useParams();
  const router = useRouter();
  const slug = params?.slug as string;
  const [job, setJob] = useState<MagazineJob | null>(null);
  const [loading, setLoading] = useState(true);

  useEffect(() => {
    fetch("/api/bots/auto-magazine")
      .then(r => r.json())
      .then(data => {
        const found = (data.jobs as MagazineJob[]).find(j => j.slug === slug);
        setJob(found ?? null);
        setLoading(false);
      })
      .catch(() => setLoading(false));
  }, [slug]);

  if (loading) {
    return (
      <div style={{ minHeight: "100vh", background: "#060410", display: "flex", alignItems: "center", justifyContent: "center" }}>
        <motion.div
          animate={{ opacity: [0.3, 1, 0.3] }}
          transition={{ repeat: Infinity, duration: 1.4 }}
          style={{ color: "#00FFFF", fontSize: 14, letterSpacing: 4 }}
        >
          LOADING ISSUE...
        </motion.div>
      </div>
    );
  }

  if (!job) {
    return (
      <div style={{ minHeight: "100vh", background: "#060410", display: "flex", flexDirection: "column", alignItems: "center", justifyContent: "center", gap: 16 }}>
        <div style={{ color: "#FF2DAA", fontSize: 20, fontWeight: 900, letterSpacing: 3 }}>ARTICLE NOT FOUND</div>
        <div style={{ color: "#444", fontSize: 13 }}>Slug: {slug}</div>
        <Link href="/magazine" style={{ color: "#00FFFF", fontSize: 12, letterSpacing: 2 }}>← BACK TO MAGAZINE</Link>
      </div>
    );
  }

  const sections = buildSections(job.templateId, job.variables);
  const templateLabel = job.templateId.replace(/-/g, " ").toUpperCase();

  return (
    <div style={{ minHeight: "100vh", background: "#060410", padding: "0 0 80px" }}>
      {/* Nav */}
      <div style={{ padding: "16px 20px", display: "flex", alignItems: "center", gap: 14, borderBottom: "1px solid #111" }}>
        <Link href="/magazine" style={{ color: "#444", fontSize: 11, letterSpacing: 2, textDecoration: "none" }}>
          ← MAGAZINE
        </Link>
        <span style={{ color: "#1a1a1a" }}>/</span>
        <span style={{ color: "#AA2DFF", fontSize: 11, letterSpacing: 2 }}>{templateLabel}</span>
        <div style={{ marginLeft: "auto", display: "flex", gap: 6 }}>
          <span style={{
            padding: "3px 9px", borderRadius: 4, fontSize: 10, letterSpacing: 1,
            background: job.status === "published" ? "rgba(0,255,136,0.15)" : "rgba(255,165,0,0.15)",
            color: job.status === "published" ? "#00FF88" : "#FFA500",
            border: `1px solid ${job.status === "published" ? "rgba(0,255,136,0.3)" : "rgba(255,165,0,0.3)"}`,
          }}>
            {job.status.toUpperCase()}
          </span>
        </div>
      </div>

      {/* Hero masthead */}
      <motion.div
        initial={{ opacity: 0, y: -24 }}
        animate={{ opacity: 1, y: 0 }}
        transition={{ duration: 0.5 }}
        style={{
          padding: "40px 20px 32px",
          background: "linear-gradient(180deg, rgba(170,45,255,0.12) 0%, transparent 100%)",
          borderBottom: "1px solid rgba(170,45,255,0.15)",
        }}
      >
        <div style={{ maxWidth: 900, margin: "0 auto" }}>
          <div style={{ color: "#AA2DFF", fontSize: 11, letterSpacing: 4, marginBottom: 10 }}>
            TMI MAGAZINE · AUTO-GENERATED
          </div>
          <h1 style={{
            fontSize: "clamp(22px, 5vw, 42px)", fontWeight: 900, letterSpacing: 2,
            color: "#FFFFFF", margin: "0 0 12px", lineHeight: 1.15,
          }}>
            {templateLabel}
          </h1>
          {Object.keys(job.variables).length > 0 && (
            <div style={{ display: "flex", gap: 10, flexWrap: "wrap" }}>
              {Object.entries(job.variables).map(([k, v]) => (
                <span key={k} style={{ fontSize: 12, color: "#00FFFF", background: "rgba(0,255,255,0.08)", border: "1px solid rgba(0,255,255,0.2)", borderRadius: 4, padding: "3px 9px", letterSpacing: 1 }}>
                  {v}
                </span>
              ))}
            </div>
          )}
          <div style={{ color: "#333", fontSize: 11, marginTop: 14, letterSpacing: 1 }}>
            {new Date(job.createdAt).toLocaleDateString("en-US", { month: "long", day: "numeric", year: "numeric" })} · {job.slug}
          </div>
        </div>
      </motion.div>

      {/* Bento grid */}
      <div style={{ maxWidth: 900, margin: "32px auto 0", padding: "0 20px" }}>
        <div style={{
          display: "grid",
          gridTemplateColumns: "repeat(auto-fill, minmax(260px, 1fr))",
          gap: 20,
        }}>
          {sections.map((sec, i) => {
            const accent = ACCENT_COLORS[sec.accentIndex % ACCENT_COLORS.length];
            const clip = CLIP_PATHS[i % CLIP_PATHS.length];
            return (
              <motion.div
                key={i}
                initial={{ opacity: 0, y: 30 }}
                animate={{ opacity: 1, y: 0 }}
                transition={{ delay: i * 0.1, duration: 0.45 }}
                whileHover={{ scale: 1.02, y: -4 }}
                style={{
                  clipPath: clip,
                  background: `linear-gradient(135deg, ${accent}18 0%, rgba(6,4,16,0.95) 60%)`,
                  border: `1px solid ${accent}40`,
                  padding: "28px 24px 32px",
                  position: "relative",
                  overflow: "hidden",
                  minHeight: 180,
                }}
              >
                {/* Glow dot */}
                <div style={{
                  position: "absolute", top: 16, right: 16, width: 8, height: 8,
                  borderRadius: "50%", background: accent,
                  boxShadow: `0 0 12px ${accent}`,
                }} />

                <div style={{ fontSize: 28, marginBottom: 12 }}>{sec.icon}</div>
                <h2 style={{
                  color: accent, fontSize: 12, fontWeight: 900, letterSpacing: 3,
                  margin: "0 0 10px", textTransform: "uppercase",
                }}>
                  {sec.title}
                </h2>
                <p style={{ color: "#888", fontSize: 13, lineHeight: 1.65, margin: 0 }}>
                  {sec.body}
                </p>

                {/* Bottom accent bar */}
                <div style={{
                  position: "absolute", bottom: 0, left: 0, right: 0, height: 2,
                  background: `linear-gradient(90deg, transparent, ${accent}, transparent)`,
                }} />
              </motion.div>
            );
          })}
        </div>

        {/* Footer action */}
        <motion.div
          initial={{ opacity: 0 }}
          animate={{ opacity: 1 }}
          transition={{ delay: 0.6 }}
          style={{ marginTop: 40, textAlign: "center" }}
        >
          <div style={{ color: "#222", fontSize: 11, letterSpacing: 3, marginBottom: 16 }}>
            PUBLISH · SHARE · ARCHIVE
          </div>
          <div style={{ display: "flex", gap: 12, justifyContent: "center", flexWrap: "wrap" }}>
            <motion.button
              whileTap={{ scale: 0.95 }}
              onClick={() => router.push("/magazine")}
              style={{
                padding: "10px 24px", background: "transparent",
                border: "1px solid #333", borderRadius: 6, color: "#555",
                fontSize: 11, letterSpacing: 2, cursor: "pointer",
              }}
            >
              ← BACK TO MAGAZINE
            </motion.button>
            <motion.button
              whileTap={{ scale: 0.95 }}
              onClick={() => {
                if (typeof navigator !== "undefined") {
                  navigator.clipboard.writeText(window.location.href).catch(() => {});
                }
              }}
              style={{
                padding: "10px 24px",
                background: "linear-gradient(135deg, #AA2DFF, #00FFFF)",
                border: "none", borderRadius: 6, color: "#fff",
                fontSize: 11, letterSpacing: 2, cursor: "pointer", fontWeight: 700,
              }}
            >
              COPY LINK
            </motion.button>
          </div>
        </motion.div>
      </div>
    </div>
  );
}
