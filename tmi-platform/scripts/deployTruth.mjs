#!/usr/bin/env node
/**
 * deployTruth.mjs
 * Pre-deployment smoke gate: env, build, adapter, asset paths, middleware.
 * Run: node scripts/deployTruth.mjs [--url https://your-domain.com]
 */

import { execSync } from "child_process";
import { existsSync } from "fs";
import { resolve, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "..");
const WEB = resolve(ROOT, "apps/web");

const CRITICAL_ROUTES = [
  "/",
  "/login",
  "/signup",
  "/home/1",
  "/shows",
  "/events",
  "/magazine",
  "/live",
];

const REQUIRED_FILES = [
  "apps/web/.next/routes-manifest.json",  // generated by every next build
  "apps/web/.next/server/pages-manifest.json",
  "apps/web/next.config.js",
  "apps/web/middleware.ts",
  "apps/web/src/app/layout.tsx",
  "apps/web/src/app/page.tsx",
];

const REQUIRED_SCRIPTS = [
  "scripts/guard-build-env.mjs",
  "scripts/envTruth.mjs",
];

function run(cmd, opts = {}) {
  try {
    return execSync(cmd, { encoding: "utf8", cwd: ROOT, stdio: "pipe", ...opts });
  } catch (e) {
    return null;
  }
}

function pass(name, detail = "") {
  console.log(`  ✓ [PASS] ${name.padEnd(44)} ${detail}`);
  return { name, status: "pass" };
}

function fail(name, detail = "") {
  console.log(`  ✗ [FAIL] ${name.padEnd(44)} ${detail}`);
  return { name, status: "fail", detail };
}

function warn(name, detail = "") {
  console.log(`  ⚠ [WARN] ${name.padEnd(44)} ${detail}`);
  return { name, status: "warn" };
}

async function checkRoute(baseUrl, path) {
  try {
    const res = await fetch(`${baseUrl}${path}`, {
      redirect: "manual",
      signal: AbortSignal.timeout(8000),
      headers: { "User-Agent": "TMI-DeployTruth/1.0" },
    });
    return { path, status: res.status, ok: res.status < 500 };
  } catch (err) {
    return { path, status: 0, ok: false, error: String(err) };
  }
}

async function main() {
  const args = process.argv.slice(2);
  const urlIdx = args.indexOf("--url");
  const targetUrl = urlIdx >= 0 ? args[urlIdx + 1] : null;

  console.log("\n═══ DEPLOY TRUTH ════════════════════════════════");
  console.log(`  Root    : ${ROOT}`);
  console.log(`  Target  : ${targetUrl ?? "(local check only)"}`);
  console.log("─────────────────────────────────────────────────");

  const results = [];

  // ── File presence ─────────────────────────────────────────────────────────
  console.log("\n  [File presence]");
  for (const f of REQUIRED_FILES) {
    const full = resolve(ROOT, f);
    results.push(existsSync(full) ? pass(f) : fail(f, "missing"));
  }
  for (const s of REQUIRED_SCRIPTS) {
    const full = resolve(ROOT, s);
    results.push(existsSync(full) ? pass(s) : warn(s, "missing"));
  }

  // ── Build check ────────────────────────────────────────────────────────────
  console.log("\n  [Build artifacts]");
  const nextBuildExists = existsSync(resolve(WEB, ".next"));
  results.push(nextBuildExists ? pass(".next/ directory") : fail(".next/ directory", "run pnpm build first"));

  // required-server-files.json only exists in standalone output mode
  const serverFilesPath = resolve(WEB, ".next/required-server-files.json");
  const routesManifestPath = resolve(WEB, ".next/routes-manifest.json");
  if (existsSync(serverFilesPath)) {
    const { readFileSync } = await import("fs");
    try {
      const sf = JSON.parse(readFileSync(serverFilesPath, "utf8"));
      results.push(sf.version ? pass("required-server-files.json", `v${sf.version}`) : warn("required-server-files.json", "no version field"));
      results.push(pass("Output mode", sf.config?.output ?? "default"));
    } catch {
      results.push(fail("required-server-files.json", "parse error"));
    }
  } else if (existsSync(routesManifestPath)) {
    const { readFileSync } = await import("fs");
    try {
      const rm = JSON.parse(readFileSync(routesManifestPath, "utf8"));
      const routeCount = (rm.staticRoutes?.length ?? 0) + (rm.dynamicRoutes?.length ?? 0);
      results.push(pass("Output mode", `default — ${routeCount} routes in manifest`));
    } catch {
      results.push(warn("routes-manifest.json", "parse error"));
    }
  }

  // ── Typecheck — web app only, skipLibCheck matches next build's ignoreBuildErrors:true ──
  console.log("\n  [TypeScript]");
  const tsOut = run(`node ${JSON.stringify(resolve(WEB, "../../node_modules/typescript/bin/tsc"))} --noEmit --skipLibCheck -p ${JSON.stringify(resolve(WEB, "tsconfig.json"))}`);
  results.push(tsOut !== null ? pass("tsc --noEmit (web)") : fail("tsc --noEmit (web)", "errors — run tsc --noEmit in apps/web"));

  // ── Env vars ───────────────────────────────────────────────────────────────
  console.log("\n  [Environment vars]");
  const envOut = run("node scripts/envTruth.mjs");
  results.push(envOut !== null ? pass("envTruth.mjs") : warn("envTruth.mjs", "some vars missing"));

  // ── Live route smoke (optional) ────────────────────────────────────────────
  if (targetUrl) {
    console.log(`\n  [Route smoke — ${targetUrl}]`);
    for (const route of CRITICAL_ROUTES) {
      const result = await checkRoute(targetUrl, route);
      results.push(result.ok
        ? pass(`GET ${route}`, `HTTP ${result.status}`)
        : fail(`GET ${route}`, `HTTP ${result.status}${result.error ? ` — ${result.error}` : ""}`)
      );
    }
  }

  // ── Summary ────────────────────────────────────────────────────────────────
  console.log("\n═════════════════════════════════════════════════");
  const passed = results.filter((r) => r.status === "pass").length;
  const failed = results.filter((r) => r.status === "fail").length;
  const warned = results.filter((r) => r.status === "warn").length;
  console.log(`  PASS: ${passed}  WARN: ${warned}  FAIL: ${failed}`);

  if (failed > 0) {
    console.log("\n  DEPLOY BLOCKED — fix failures above.\n");
    process.exit(1);
  }
  if (warned > 0) {
    console.log("\n  DEPLOY ALLOWED WITH WARNINGS — review above.\n");
    process.exit(0);
  }
  console.log("\n  DEPLOY READY\n");
  process.exit(0);
}

main();
